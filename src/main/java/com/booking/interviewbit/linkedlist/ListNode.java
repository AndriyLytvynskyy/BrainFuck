package com.booking.interviewbit.linkedlist;

/**
 * Given a singly linked list, determine if its a palindrome.
 * Return 1 or 0 denoting if its a palindrome or not, respectively.
 */
//Solution is borrowed from:
// https://www.sigmainfy.com/blog/leetcode-palindrome-linked-list-o1-space.html
class ListNode {
      public int val;
      public ListNode next;
      ListNode(int x) { val = x; next = null; }
}
